package org.ssh.torch;

/**
 * The Enum TorchScope.
 *
 * This enumeration enumerates the different types of event scopes.
 *
 * @author Jeroen de Jong
 */
public enum TorchScope {
  /**
   * Component scope.
   */
  COMPONENT,
  /**
   * Window scope.
   */
  WINDOW,
  /**
   * Workspace scope.
   */
  WORKSPACE,
  /**
   * Terminal scope.
   */
  TERMINAL
}
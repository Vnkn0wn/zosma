package org.ssh.torch;

/**
 * The Enum TorchAction.
 *
 * This enumeration enumerates the different types of event actions.
 *
 * @author Jeroen de Jong
 */
public enum TorchAction {
    /**
     * Resize action.
     */
    RESIZE,
    /**
     * Switch action.
     */
    SWITCH,
    /**
     * Created action.
     */
    CREATED,
    /**
     * Removed action.
     */
    REMOVED,
    /**
     * Closed action.
     */
    CLOSED
}